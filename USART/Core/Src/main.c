  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  /* USER CODE BEGIN 2 */

  // инициализация UART1

  RCC->APB2ENR |= RCC_APB2ENR_USART1EN; // включаем тактирование UART1
  //UART1 использует выводы: PA9 (для сигнала TX) и PA10 (сигнал RX)
  RCC->APB2ENR |= RCC_APB2ENR_IOPAEN; // разрешаем тактирование порта GPIOA
  // настройка вывода PA9 (TX1) на  выход
  GPIOA -> AFR[1] |= (0b0111 << GPIO_AFRH_AFSEL10_Pos) | (0b0111 << GPIO_AFRH_AFSEL9_Pos);
  GPIOA -> MODER |= (0b10 << GPIO_MODER_MODE10_Pos) | (0b10 << GPIO_MODER_MODE9_Pos);

  // конфигурация UART1
  USART1->CR1 = USART_CR1_UE; // разрешаем USART1, сбрасываем остальные биты
